import { Hono } from "hono";

const uploadRouter = new Hono();

uploadRouter.post("/", async (c) => {
  const formData = await c.req.formData();
  const file = formData.get("file");

  if (!file || !(file instanceof File)) {
    return c.json({ error: "No file provided" }, 400);
  }

  const storageForm = new FormData();
  storageForm.append("file", file);

  const response = await fetch("https://storage.vibecodeapp.com/v1/files/upload", {
    method: "POST",
    body: storageForm,
  });

  if (!response.ok) {
    const error = await response.json() as { error?: string };
    return c.json({ error: error.error || "Upload failed" }, 500);
  }

  const result = await response.json() as { file: { url: string; id: string; originalFilename: string } };
  return c.json({ url: result.file.url, id: result.file.id, filename: result.file.originalFilename });
});

export { uploadRouter };
